// операторы присваивания

let age = 18
age = 18 + 5
age += 2 // age = age + 2
age -= 3 // age = age - 3
age *= 2 // age = age * 2
age /= 2 // age = age / 2

age++ // age = age + 1
age-- // age = age - 1
console.log(age)

// операторы сравнения
const vasia = 20
console.log(age > vasia) // больше
console.log(age >= vasia) // больше либо равно
console.log(age < vasia) // меньше
console.log(age <= vasia) // меньше либо равно
console.log(age == vasia) // равно
